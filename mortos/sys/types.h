typedef int8_t		int8;

/*!
 *  \typedef	uint16
 *  \brief	16 bits unsigned int
 */
typedef uint16_t	uint16;

/*!
 *  \typedef	int16
 *  \brief	16 bits signed int
 */
typedef int16_t		int16;

/*!
 *  \typedef	uint32
 *  \brief	32 bits unsigned int
 */
typedef uint32_t	uint32;

/*!
 *  \typedef    int32
 *  \brief      32 bits signed int
 */
typedef int32_t		int32;

/*!
 *  \typedef    uint64
 *  \brief      64 bits unsigned int
 */
typedef uint64_t	uint64;

/*!
 *  \typedef    int64
 *  \brief      64 bits signed int
 */
typedef int64_t		int64;

#endif
